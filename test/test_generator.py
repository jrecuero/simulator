#------------------------------------------------------------------------------
class Test(unittest.TestCase):

    #--------------------------------------------------------------------------
    def setUp(self):
        self.cb  = mock.Mock()
        self.eng = engine.Engine()
        self.gen = generator.Generator(self.eng, 'test-gen', cb=self.cb, limit=10)
        
    #--------------------------------------------------------------------------
    def tearDown(self):
        pass

    #--------------------------------------------------------------------------
    def test_init(self):
        pass

    #--------------------------------------------------------------------------
    def test_run(self):
        # Test
        self.gen.next()
        self.eng.runEngine()
        
        # Expectations
        self.assertEqual(self.cb.call_count, 10, 'callback was not called 10 times')
        

